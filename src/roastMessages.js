const TIER_KEYS = [
  { max: 2.0, key: "fail", tone: "fail" },
  { max: 2.5, key: "disaster", tone: "fail" },
  { max: 3.0, key: "pathetic", tone: "warn" },
  { max: 3.5, key: "mid", tone: "warn" },
  { max: 3.8, key: "acceptable", tone: "neutral" },
  { max: 4.0, key: "strong", tone: "great" },
];

export function getRoastMessage(sgpa, recentRoasts = []) {
  const tier = TIER_KEYS.find((t) => sgpa < t.max) ?? {
    key: "perfect",
    tone: "perfect",
  };
  const pool = ROAST_MESSAGES[tier.key];
  const fresh = pool.filter((msg) => !recentRoasts.includes(msg));
  const choices = fresh.length > 0 ? fresh : pool;
  const text = choices[Math.floor(Math.random() * choices.length)];
  return { text, tone: tier.tone };
}
